package ConjuntoParciales.Ejercicio1;

public class ElementoEj1 {

        private char car;
        private int valor;

        public ElementoEj1(char car, int valor) {
            this.car = car;
            this.valor = valor;
        }

        public char getCar() {
            return car;
        }

        public void setCar(char car) {
            this.car = car;
        }

        public int getValor() {
            return valor;
        }

        public void setValor(int valor) {
            this.valor = valor;
        }

}
